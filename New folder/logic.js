let a=10;
let b=20;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);
console.log(a++); // Postfix increment
console.log(++a); // Prefix increment
console.log(a--); // Postfix decrement
console.log(--a); // Prefix decrement
console.log(a===b); // Strict equality
console.log(a!==b); // Strict inequality
console.log(a==b); // Loose equality
console.log(a!=b); // Loose inequality
console.log(a<b); // Less than
 